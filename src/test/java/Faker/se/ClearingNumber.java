package Faker.se;

public class ClearingNumber {
    private Integer min;
    private Integer max;
    private Integer numbersToGenerate;

    public Integer getMin() {
        return min;
    }

    public void setMin(Integer min) {
        this.min = min;
    }

    public Integer getMax() {
        return max;
    }

    public void setMax(Integer max) {
        this.max = max;
    }

    public Integer getNumbersToGenerate() {
        return numbersToGenerate;
    }

    public void setNumbersToGenerate(Integer numbersToGenerate) {
        this.numbersToGenerate = numbersToGenerate;
    }
}
